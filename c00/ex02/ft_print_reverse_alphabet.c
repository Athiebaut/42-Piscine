#include <unistd.h>

void	ft_write(char c)
{
	write(1, &c, 1);
}

void	ft_print_reverse_alphabet(void)
{
	char	c;

	c = 'z';
	while (c >= 'a')
	{
		ft_write(c);
		c--;
	}
}
