#include <unistd.h>

void	ft_write(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	unsigned int	n;

	n = nb;
	if (nb < 0)
	{
		ft_write('-');
	}
	if (n >= 9)
	{
		ft_putnbr(n / 10);
	}
	ft_write(n % 10 + '0');
}
